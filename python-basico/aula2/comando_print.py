"""
Aula 2 - Comando print

Função utilizada para exibir texto na tela (saída do prompt de comando).

Argumentos:

values: string ou conjunto de strings separadas por vírgula.
sep: string inserida no meio de cada string, seu valor padrão é um espaço em branco (" ").
end: string inserida no final, seu valor padrão é a quebra de linha (\n).
"""

print("Romeu", "e", "Julieta")
print("Valor:", "R$15")

print("Bem", "Vindo", sep="-")

print("Passa", end="")
